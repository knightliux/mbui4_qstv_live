package com.moon.android.live.custom007.white;

public class ChianPull {
	public String chian;
	public String ip;

	public String getChian() {
		return chian;
	}

	public void setChian(String chian) {
		this.chian = chian;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	@Override
	public String toString() {
		return "chianpull [chian=" + chian + ", ip=" + ip + "]";
	}

}
